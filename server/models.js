var path = require('path');
var jbase = require('jbase');

//----------------------------------------------------------------------------------------------------------------------

var db = { errors: jbase.errors };
var rootPath = path.join(__dirname, 'db');

//----------------------------------------------------------------------------------------------------------------------
// Site models
//----------------------------------------------------------------------------------------------------------------------

db.User = jbase.defineModel('users', {
    gPlusID: String,
    nickname: String,
    tagline: String,
    email: String,
    displayName: String,
    avatar: String,
    created: { type: Number, default: Date.now() }
}, { rootPath: rootPath });

//----------------------------------------------------------------------------------------------------------------------

module.exports = db;

